package com.pendakers.Model;

public class DataBerau {
    public String id, nomor_mou, nomor_pks, tanggal_pks, masa_berlaku, jangka_waktu, unit_kerja, mitra_kerja, tentang, jenis, data_file;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNomor_mou() {
        return nomor_mou;
    }

    public void setNomor_mou(String nomor_mou) {
        this.nomor_mou = nomor_mou;
    }

    public String getNomor_pks() {
        return nomor_pks;
    }

    public void setNomor_pks(String nomor_pks) {
        this.nomor_pks = nomor_pks;
    }

    public String getTanggal_pks() {
        return tanggal_pks;
    }

    public void setTanggal_pks(String tanggal_pks) {
        this.tanggal_pks = tanggal_pks;
    }

    public String getMasa_berlaku() {
        return masa_berlaku;
    }

    public void setMasa_berlaku(String masa_berlaku) {
        this.masa_berlaku = masa_berlaku;
    }

    public String getJangka_waktu() {
        return jangka_waktu;
    }

    public void setJangka_waktu(String jangka_waktu) {
        this.jangka_waktu = jangka_waktu;
    }

    public String getUnit_kerja() {
        return unit_kerja;
    }

    public void setUnit_kerja(String unit_kerja) {
        this.unit_kerja = unit_kerja;
    }

    public String getMitra_kerja() {
        return mitra_kerja;
    }

    public void setMitra_kerja(String mitra_kerja) {
        this.mitra_kerja = mitra_kerja;
    }

    public String getTentang() {
        return tentang;
    }

    public void setTentang(String tentang) {
        this.tentang = tentang;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getData_file() {
        return data_file;
    }

    public void setData_file(String data_file) {
        this.data_file = data_file;
    }
}
